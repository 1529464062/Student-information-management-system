		public List<Maticsoft.Model.student_info> DataTableToList(DataTable dt)
		{
			List<Maticsoft.Model.student_info> modelList = new List<Maticsoft.Model.student_info>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Maticsoft.Model.student_info model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

